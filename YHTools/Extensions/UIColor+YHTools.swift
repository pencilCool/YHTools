import UIKit

extension UIColor {
    class func yhTintColor() -> UIColor {
        return #colorLiteral(red: 0.1607843137, green: 0.5176470588, blue: 0.2666666667, alpha: 1)
    }
    
    class func yhFlashlightColor() -> UIColor {
        return #colorLiteral(red: 1, green: 0.1491314173, blue: 0, alpha: 1)
    }
    
}
